package persona;

public class Persona
{
	protected float peso;
	protected float altura;
	
	public void persona()
	{
		
	}
	public void setPeso(float miPeso)
	{
		peso = miPeso;
	}
	public float getPeso()
	{
		return peso;
	}
	public void setAltura(float miAltura)
	{
		altura = miAltura;
	}
	public float getAltura()
	{
		return altura;
	}
}